////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include "timers.h"

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

unsigned char *VIA_T1CL = (unsigned char *)0x8004;
unsigned char *VIA_T1CH = (unsigned char *)0x8005;
unsigned char *VIA_ACR = (unsigned char *)0x800b;
unsigned char *VIA_IER = (unsigned char *)0x800e;

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void timers_init() {
    *VIA_ACR = 0b01000000;
    *VIA_IER = 0b11000000;
    *VIA_T1CH = 0xff;
    *VIA_T1CL = 0xff;
}

